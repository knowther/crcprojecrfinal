        if( m == 0){
            novoButtonSessao.setEnabled(false);
            jButtonEditarSessao.setEnabled(false);
        }

        if(sessaoList.isEmpty() == true){
            novoButtonSessao.setEnabled(false);
            jButtonEditarSessao.setEnabled(false);
            jButtonExcluirSessao.setEnabled(false);
            jButtonSalvarSessao.setEnabled(false);
        }

